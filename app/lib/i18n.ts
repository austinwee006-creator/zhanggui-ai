export function normalizeLanguage(value: unknown): Language {
  return value === "en" ? "en" : "zh";
}

export function loadLanguage(): Language {
  if (typeof window === "undefined") return "zh";
  return normalizeLanguage(window.localStorage.getItem(languageStorageKey));
}

export function saveLanguage(language: Language) {
  if (typeof window === "undefined") return;
  window.localStorage.setItem(languageStorageKey, language);
}

export function translateText(text: string, language: Language) {
  if (language === "zh" || !text.trim()) return text;

  return sortedPhrasePairs.reduce((result, [source, target]) => result.split(source).join(target), text);
}
